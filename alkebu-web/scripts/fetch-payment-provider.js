import fs from 'node:fs/promises';
import path from 'node:path';

const baseUrl = process.env.PAYLOAD_BASE_URL || 'http://localhost:3000';
const outputPath = path.resolve(process.cwd(), 'src/lib/paymentProvider.ts');

async function main() {
  try {
    const res = await fetch(`${baseUrl}/api/payment-methods`);
    if (!res.ok) {
      throw new Error(`Failed to fetch payment methods: ${res.status} ${res.statusText}`);
    }

    const data = await res.json();
    const slug = data.defaultProvider || data.methods?.[0]?.slug || 'stripe';
    const method = data.methods?.find((m) => m.slug === slug);

    const payload = {
      slug,
      name: method?.name || slug,
      note: method?.note || `${slug} is used to process your payment securely.`,
    };

    const fileContents = `// Generated by scripts/fetch-payment-provider.js\nexport const paymentProvider = ${JSON.stringify(
      payload,
      null,
      2,
    )} as const;\n`;

    await fs.writeFile(outputPath, fileContents, 'utf8');
    console.log(`Wrote payment provider info to ${outputPath}`);
  } catch (error) {
    console.error('Failed to fetch payment provider info:', error);

    const fallback = {
      slug: 'stripe',
      name: 'Stripe',
      note: 'Card payments are processed securely.',
    };
    const fileContents = `// Fallback generated by scripts/fetch-payment-provider.js\nexport const paymentProvider = ${JSON.stringify(
      fallback,
      null,
      2,
    )} as const;\n`;
    await fs.writeFile(outputPath, fileContents, 'utf8');
  }
}

void main();
